                                        <div class="blog" id="thumbnails">
                                          <p>- Tên khách hàng: 
                                            <strong>Ban quản lý chương trình UNREDD giai đoạn II tỉnh Bắc Kạn</strong></p><p>- Sản phẩm thiết kế &amp; in ấn: 
                                              <strong>Bộ lịch tết 7 tờ</strong></p><p>- Đơn vị thiết kế:
                                               <strong>In Hồng Đăng</strong></p><p>- Các hạng mục: 
                                                <strong>Thiết kế lịch tết</strong>, 
                                                <strong>in ấn lịch&nbsp;</strong></p>
                                                <p>&nbsp;</p>
                                                <p>UN REDD là là một trong những chương trình hợp tác Liên hợp quốc với mục đích là giảm phát thải khí nhà kính và đặc biệt là hạn chế mất rừng cũng như suy thoái rừng ở các nước đang phát triển. Việt Nam chúng ta là một trong những nước được tổ chức Liên hợp quốc này quan tâm cũng như hỗ trợ về mọi mặt. Như chúng ta đã biết Việt Nam là một nước có khí hậu nhiệt đới và là nước sở hữu rừng vàng biển bạc, nhưng lại là quốc gia đang còn nghèo và hạn chế về việc bảo vệ rừng.</p><p>Mục tiêu của chương trình UN REDD đó chính là trợ giúp Việt Nam thực hiện việc bảo vệ rừng một cách tốt hơn, cụ thể hơn là giảm phát thải khí nhà kính và thông qua nỗ lực hạn chế mất rừng và suy thoái rừng. Chương trình này sẽ tăng cường năng lực và cung cấp hỗ trợ kỹ thuật ở cấp quốc gia và cấp tỉnh, đồng thời cũng bao gồm các hoạt động ban đầu để giảm lượng phát thải tại 6 tỉnh thí điểm: Lâm Đồng, Cà Mau, Bình Thuận, Hà Tĩnh, Bắc Kạn và Lào Cai.</p>
                                                <p>Và Bắc Kạn là một trong những tỉnh nằm trong dự án bảo vệ rừng mà chương trình UN REDD muốn hướng tới, vào ngày 6/9, Ban quản lý chương trình UNREDD giai đoạn II tỉnh Bắc Kạn đã phối hợp với UBND tỉnh tổ chức hội nghị công bố phê duyệt kế hoạch hành động REDD+ cấp tỉnh (còn gọi là PRAF).</p><p>&nbsp;</p>
                                                <p>
                                                  <strong>In Hồng Đăng</strong> chúng tôi đã rất may mắn khi được Ban quản lý chương trình UNREDD giai đoạn II tỉnh Bắc Kạn đã đặt niềm tin và lựa chọn là 
                                                  <a href="http://inhongdang.com.vn/">
                                                    <strong>công ty&nbsp;thiết kế và in&nbsp;lịch tết</strong></a>. Những cuốn
                                                    <strong>lịch tết </strong>này được in theo yêu cầu về chất lượng cũng như về mẫu mã đã dạng và chuyên nghiệp.</p><p>&nbsp;</p>
                                                    <p>
                                                      <strong><span style="font-size:16px">Quy trịnh thiết kế và in lịch tết độc quyền</span></strong></p>
                                                      <p>Dưới đây là quy trình hình thành nên những cuốn 
                                                        <strong>lịch tết</strong> mà 
                                              <strong>Công ty In</strong> Hồng Đăng đã thực hiện cho ban quản lý chương trình UNREDD giai đoạn II tỉnh Bắc Kạn.</p>
                                                  <p>&nbsp;</p>
                                                 <p>Qúa trình sản xuất hoàn chỉnh những cuốn 
                                                <strong>lịch tết đẹp</strong>&nbsp;được chia làm 7 khâu:</p><p>✔ Khâu thứ nhất: Là thống nhất về nội dung 
                                              <strong>in lịch tết</strong>, rồi sau đó tiến hành ký kết hợp đồng giữa hai bên, sau đó khách hàng sẽ tiến hành thanh toán trước khoảng 30 - 50 % giá trị hợp đồng cho công ty in ấn Hồng Đăng. Đây là bước hết sức quan trọng để quyết định hai bên có hợp tác được với nhau hay không.</p>
                                              <p>✔ Khâu thứ hai: Lên ý tưởng và biên soạn nội dung cho cuốn
                                             <strong>lịch tết độc quyền</strong>, bước này khách hàng sẽ gửi nội dung để phòng thiết kế biên soạn hay nội dung sẽ do&nbsp;phòng thiết kế tự sáng tạo, &nbsp;đặc biệt là phải có sự thống nhất giữa khách hàng và 
                                             <strong>công ty&nbsp;in ấn</strong>.</p>
                                            <p>✔ Khâu thứ ba: Phòng đồ họa sẽ lên ý tưởng và tiến hành thiết kế để lên maket
                                           <strong>lịch tết</strong>, để làm vừa lòng khách hàng ở bước này đòi hỏi đội ngũ thiết kế phải rất sáng tạo để đưa ra bản maket thuyết phục khách hàng.</p>
                                             <p>✔ Khâu thứ tư: Gửi bản market cho khách hàng qua email để khách hàng xem và gó ý cũng như duyệt,, nếu khách hàng đã hài lòng thì phản hồi email là ‘Ok maket’</p><p>✔ Khâu thứ năm: Thực hiện in và gia công lịch tết, đây là bước chuyển từ file thiết kế thành sản phẩm thật.</p>
                                              <p>✔ Khâu thứ sáu: Giao lịch tết cho khách hàng, ở bước này bộ phận giao hàng sẽ giao hàng tới địa chỉ mà khách hàng yêu cầu.</p><p>✔ Khâu thứ bảy: Khách hàng duyệt thành phẩm và thanh toán nốt số tiền còn lại theo hợp đồng đã ký giữa hai bên.</p>
